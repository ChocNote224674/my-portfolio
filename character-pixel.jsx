// Pixel-art character — 32×36 grid at 10px per pixel = 320×360 viewBox.
// Same identity cues as the soft style: buzz cut, full beard, olive skin,
// teal shirt, stud piercing in user's left ear (viewer's right).
//
// Pose strings: 32 chars per row, 36 rows. Each glyph maps to a colour.
// Empty space = '.'.
//
// Palette glyphs:
//   H = hair / beard / moustache (dark)
//   S = skin (face)
//   s = skin shade
//   b = brow (same hair colour)
//   E = eye (ink)
//   T = shirt (accent-ink)
//   t = shirt highlight (accent)
//   W = paper / collar
//   P = pants (dark grey)
//   O = outline / shoes (ink)
//   G = gold stud
//   L = laptop frame (light grey)
//   l = laptop screen (dark)
//   c = code line cyan
//   y = code line yellow
//   r = code line red
//   K = headphone cup (dark)
//   k = headphone speaker (accent)
//   m = mic boom
//   M = magnifier glass
//   R = megaphone red (yellow-orange)
//   Y = trophy gold
//   V = VR visor (paper)
//   v = VR lens (dark)
//   z = soundwave / sparkle (accent ink)

