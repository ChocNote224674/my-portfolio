function renderPixelArt(art, palette, pixelSize = 10) {
  const rows = art.split('\n').filter(r => r.length > 0);
  const rects = [];
  let key = 0;
  for (let y = 0; y < rows.length; y++) {
    const row = rows[y];
    for (let x = 0; x < row.length; x++) {
      const ch = row[x];
      if (ch === '.' || ch === ' ') continue;
      const fill = palette[ch];
      if (!fill) continue;
      rects.push(
        <rect key={key++}
              x={x * pixelSize} y={y * pixelSize}
              width={pixelSize} height={pixelSize}
              fill={fill}
              shapeRendering="crispEdges" />
      );
    }
  }
  return rects;
}

